            <h2>Dog Care Instructions and Tips</h2>
            <p>If you're considering bringing home a new dog, please make adoption your first option. We encourage you to browse our <a href="BrowsePets.html" >directory of adoptable dogs</a> in your area.</p>
            <p>
                <h3>Feeding</h3>
                <ul id="dogFeed">
                    <li>Puppies eight to 12 weeks old need four meals a day.</li>
                    <li>Feed puppies three to six months old three meals a day.</li>
                    <li>Feed puppies six months to one year two meals a day.</li>
                    <li>When your dog reaches his first birthday, one meal a day is usually enough.</li>
                    <li>For some dogs, including larger canines or those prone to bloat, it's better to feed two smaller meals.</li>
                </ul>
            </p>
            <p>Premium-quality dry food provides a well-balanced diet for adult dogs and may be mixed with water, broth or canned food. Your dog may enjoy cottage cheese, cooked egg or fruits and vegetables, but these additions should not total more than ten percent of his daily food intake.</p>
            <p>Puppies should be fed a high-quality, brand-name puppy food (large breed puppy foods for large breeds). Please limit "people food," however, because it can result in vitamin and mineral imbalances, bone and teeth problems and may cause very picky eating habits and obesity. Clean, fresh water should be available at all times, and be sure to wash food and water dishes frequently.</p>
            <p>
                <h3>Exercise</h3>
                Dogs need exercise to burn calories, stimulate their minds, and stay healthy. Individual exercise needs vary based on breed or breed mix, sex, age and level of health. Exercise also tends to help dogs avoid boredom, which can lead to destructive behaviors. Supervised fun and games will satisfy many of your pet's instinctual urges to dig, herd, chew, retrieve and chase.
            </p>
